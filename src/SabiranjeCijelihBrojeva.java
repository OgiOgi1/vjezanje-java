public class SabiranjeCijelihBrojeva {
    public static void main(String[] args) {
        System.out.println(sabiranjeDvaCijelaBr(3,4));
        System.out.println(sabiranjeDvaBroja(3.0,5.0));

    }
    public static int sabiranjeDvaCijelaBr(int prvi, int drugi){
        return prvi + drugi;
    }


public static double sabiranjeDvaBroja(double prvibroj, double drugibroj){
    return prvibroj + drugibroj;
}
}
